public class Datatypes {
    public static void main(String[] args) {
        // byte, short, int, long, float, double, boolean, char
        int val1 = 12 / 6;
        float val2 = 12f / 10f;
        double val3 = 12 / 10;

        System.out.println("Integer: " + val1);
        System.out.println("Float: " + val2);
        System.out.println("Double: " + val3);
    }
}
